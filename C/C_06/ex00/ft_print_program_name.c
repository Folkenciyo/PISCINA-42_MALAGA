#include <unistd.h>

int	main(int argc, char *argv[])
{
	int		i;
	char	sl;

	i = 0;
	sl = '\n';
	if (argc > 0)
	{
		while (argv[0][i] != '\0')
		{
			write(1, &argv[0][i], 1);
			i++;
		}
		write(1, &sl, 1);
	}
	return (0);
}
