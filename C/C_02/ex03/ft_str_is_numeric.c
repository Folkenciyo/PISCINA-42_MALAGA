int	ft_str_is_numeric(char *str)
{
	int	n;

	n = 0;
	while ((str[n] >= '0' && str[n] <= '9'))
	{
		n++;
	}
	if (str[n] == '\0')
	{	
		return (1);
	}
	else
	{
		return (0);
	}
}
