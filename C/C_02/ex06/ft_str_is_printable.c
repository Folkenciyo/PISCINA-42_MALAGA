int	ft_str_is_printable(char *str)
{
	int	n;

	n = 0;
	while ((str[n] >= 32 && str[n] <= 126))
	{
		n++;
	}
	if (str[n] == '\0')
	{	
		return (1);
	}
	else
	{
		return (0);
	}
}
